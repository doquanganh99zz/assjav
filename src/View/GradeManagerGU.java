    private boolean checkData() {
        String studentId = txtStudentId.getText().trim();
        String eng = txtEnglishScore.getText().trim();
        String tinHoc = txtItScore.getText().trim();
        String gdtc = txtExerciseScore.getText().trim();
        if (!gmsi.isThisStudentExist(studentId)) {
            JOptionPane.showMessageDialog(this, "Sinh viên không tồn tại!");
            return false;
        }
        if (gmsi.isExist(studentId)) {
            JOptionPane.showMessageDialog(this, "Sinh viên đã có điểm!");
            return false;
        }
        String regexScore = "^([0-9]\\.[0-9])||([0-9])||(10)||(10\\.0)$";
        if (!eng.matches(regexScore)) {
            JOptionPane.showMessageDialog(this, "Điểm không đúng định dạng!");
            return false;
        }
        if (!tinHoc.matches(regexScore)) {
            JOptionPane.showMessageDialog(this, "Điểm không đúng định dạng!");
            return false;
        }
        if (!gdtc.matches(regexScore)) {
            JOptionPane.showMessageDialog(this, "Điểm không đúng định dạng!");
            return false;
        }
        return true;
    }
    
