    private void toControls(int index) {
        if (list.isEmpty()) {
            return;
        }
        Grade g = list.get(index);
        studentName.setText(g.getHoTen());
        txtStudentId.setText(g.getMaSv());
        txtEnglishScore.setText(String.valueOf(g.getTiengAnh()));
        txtItScore.setText(String.valueOf(g.getTinHoc()));
        txtExerciseScore.setText(String.valueOf(g.getGDTC()));
        averageScore.setText(String.valueOf(g.dienTB()));
    }
    
    private void tblContent() {
        DefaultTableModel dtm = (DefaultTableModel) tblMain.getModel();
        dtm.setColumnIdentifiers(new Object[]{
            "Mã SV", "Họ Tên", "Điểm TA", "Điểm Tin Học", "Điểm GDTC", "Điểm TB"
        });
        tblMain.setModel(dtm);
    }
    
    private void top3Average() {
        DefaultTableModel dtm = (DefaultTableModel) tblMain.getModel();
        dtm.setRowCount(0);
        List<Grade> listTop3 = gmsi.getTop3Score();
        for (Grade grade : listTop3) {
            Object[] ob = {grade.getMaSv(), grade.getHoTen(), grade.getTiengAnh(), grade.getTinHoc(), grade.getGDTC(), grade.dienTB()};
            dtm.addRow(ob);
        }
        tblMain.setModel(dtm);
    }
    
  

